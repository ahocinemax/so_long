#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(int))
{
	t_list	*curr;
	t_list	*next;

	(void)(*del);
	if (!lst || !*lst)
		return ;
	curr = *lst;
	while (curr)
	{
		next = curr->next;
		free(curr);
		curr = next;
	}
	*lst = NULL;
}
